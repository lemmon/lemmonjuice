  function arrow(b1, b2) {
    // horizontal
    const y1 = Math.max(b1.offsetTop, b2.offsetTop)
    const y2 = Math.min(b1.offsetTop + b1.offsetHeight, b2.offsetTop + b2.offsetHeight)
    if (y1 < y2) {
      arrowEl({
        angle: 'horizontal',
        x: b1.offsetLeft + b1.offsetWidth,
        y: y1 / 2 + y2 / 2,
        w: b2.offsetLeft - b1.offsetLeft - b1.offsetWidth,
      })
    }
    // vertical
    const x1 = Math.max(b1.offsetLeft, b2.offsetLeft)
    const x2 = Math.min(b1.offsetLeft + b1.offsetWidth, b2.offsetLeft + b2.offsetWidth)
    if (x1 < x2) {
      arrowEl({
        angle: 'vertical',
        x: x1 / 2 + x2 / 2,
        y: b1.offsetTop + b1.offsetHeight,
        h: b2.offsetTop - b1.offsetTop - b1.offsetHeight,
      })
    }
  }
  function arrowEl(props) {
